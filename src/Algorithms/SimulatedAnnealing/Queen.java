package Algorithms.SimulatedAnnealing;

/**
 *
 * @author Rumal
 */
public class Queen {
	int indexOfX, indexOfY;

	public Queen() {

	}

	public Queen(int indexOfX, int indexOfY) {
		this.indexOfX = indexOfX;
		this.indexOfY = indexOfY;
	}

	public int getIndexOfX() {
		return indexOfX;
	}

	public int getIndexOfY() {
		return indexOfY;
	}

}

